// CATALOG UTILITIES
// ============================================================================

interface PhotoMapping {
  officialUrl: string;
  fallbackSearch: string;
}

interface CatalogPhotos {
  grinders: Record<string, PhotoMapping>;
  brewers: Record<string, PhotoMapping>;
  coffees: Record<string, PhotoMapping>;
}

const photos = catalogPhotos as CatalogPhotos;

/**
 * Generate a stable ID for catalog items based on their properties
 */
export function generateCatalogId(type: "brewer" | "grinder" | "coffee", item: { name: string; brand?: string | null; roaster?: string | null }): string {
  const source = item.brand || item.roaster || "unknown";
  const slug = `${type}-${source}-${item.name}`
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "");
  return `catalog-${slug}`;
}

/**
 * Get photo URL for a catalog item
 */
function getPhotoURL(type: "brewers" | "grinders" | "coffees", id: string): string | null {
  const mapping = photos[type]?.[id];
  return mapping?.officialUrl || null;
}

/**
 * Get all brewers with generated IDs
 */
export function getCatalogBrewers(): Brewer[] {
  return brewersCatalog.map((brewer) => {
    const id = generateCatalogId("brewer", brewer);
    return {
      ...brewer,
      id,
      photoURL: getPhotoURL("brewers", id),
      createdAt: null,
      updatedAt: null,
    };
  }) as Brewer[];
}

/**
 * Get all grinders with generated IDs
 */
